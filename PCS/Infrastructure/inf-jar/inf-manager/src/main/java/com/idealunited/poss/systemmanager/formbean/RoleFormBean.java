package com.idealunited.poss.systemmanager.formbean;

public class RoleFormBean {

	private String roleKy;
	private String roleCode;
	private String roleName;
	private String roleRemarks;
	private String status;

	public String getRoleKy() {
		return roleKy;
	}

	public void setRoleKy(String roleKy) {
		this.roleKy = roleKy;
	}

	public String getRoleCode() {
		return roleCode;
	}

	public void setRoleCode(String roleCode) {
		this.roleCode = roleCode;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getRoleRemarks() {
		return roleRemarks;
	}

	public void setRoleRemarks(String roleRemarks) {
		this.roleRemarks = roleRemarks;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
